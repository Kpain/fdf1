#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	unsigned int i;
	unsigned int s;

	i = 0;
	s = ft_strlen(s1);
	while (s2[i] != '\0' && i < n)
	{
		s1[s + i] = s2[i];
		i++;
	}
	s1[s + i] = '\0';
	return (s1);
}
