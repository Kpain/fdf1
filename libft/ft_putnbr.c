#include "libft.h"

void	ft_putnbr(int nb)
{
	char n;

	if (nb == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	n = nb % 10 + '0';
	ft_putchar(n);
}
